    private int pronadjiDobavljacIdPoNazivu(String naziv) throws SQLException {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement("SELECT id FROM dobavljaci WHERE naziv = ?");
        ps.setString(1, naziv);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) return rs.getInt("id");
        throw new SQLException("Dobavljač nije pronađen: " + naziv);
    }

    private int pronadjiLijekIdPoNazivu(String naziv) throws SQLException {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement("SELECT id FROM lijek WHERE naziv = ?");
        ps.setString(1, naziv);
        ResultSet rs = ps.executeQuery();
        if (rs.next()) return rs.getInt("id");
        throw new SQLException("Lijek nije pronađen: " + naziv);
    }

    private double izracunajUkupnoNarudzbe() {
        double suma = 0;
        for (int i = 0; i < stavkeModel.getRowCount(); i++) {
            String ukupnoStr = stavkeModel.getValueAt(i, 3).toString()
                    .replace(" KM", "")
                    .replace(",", ".");
            try {
                suma += Double.parseDouble(ukupnoStr);
            } catch (Exception ignored) {}
        }
        return suma;
    }
    
    private void ucitajSveLijekove() {
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();

            // Možeš ograničiti broj redova da ne učitava 5000+ lijekova odjednom
            // Ako želiš sve → samo izbriši LIMIT
            String query = """
                SELECT 
                    id, 
                    atc_sifra, 
                    naziv, 
                    proizvodjac, 
                    cijena
                FROM lijek 
                ORDER BY naziv ASC
                LIMIT 300
            """;

            ResultSet rs = st.executeQuery(query);

            // Očisti staro prije punjenja
            DefaultTableModel model = (DefaultTableModel) txtLijekovi.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[] {
                    rs.getInt("id"),
                    rs.getString("atc_sifra") != null ? rs.getString("atc_sifra") : "",
                    rs.getString("naziv"),
                    rs.getString("proizvodjac") != null ? rs.getString("proizvodjac") : "",
                    String.format("%.2f KM", rs.getDouble("cijena"))
                });
            }

            // Opcionalno: ako nema rezultata
            if (model.getRowCount() == 0) {
                JOptionPane.showMessageDialog(this, 
                    "Nema lijekova u bazi ili nisu učitani.", 
                    "Upozorenje", 
                    JOptionPane.WARNING_MESSAGE);
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(this, 
                "Greška pri učitavanju lijekova:\n" + ex.getMessage(), 
                "Greška", 
                JOptionPane.ERROR_MESSAGE);
        }
    }

    private void postaviComboboxDobavljace() {
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT id, naziv FROM dobavljaci ORDER BY naziv");

            cmbDobavljac.removeAllItems();
            while (rs.next()) {
                cmbDobavljac.addItem(rs.getString("naziv"));
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, e);
        }
    }
    private void postaviModelStavki() {
        stavkeModel = new DefaultTableModel(
            new String[]{"Lijek", "Količina", "Nab. cijena", "Ukupno", " "},  // zadnja kolona za dugme
            0
        ) {
            @Override
            public boolean isCellEditable(int row, int col) {
                return col == 1; // samo količina se može mijenjati direktno u tabeli
            }

            @Override
            public Class<?> getColumnClass(int columnIndex) {
                if (columnIndex == 1) return Integer.class;
                if (columnIndex == 2 || columnIndex == 3) return String.class; // cijene kao string zbog " KM"
                return Object.class;
            }
        };

        tblStavke.setModel(stavkeModel);

        // Postavi renderer i editor SAMO na zadnju kolonu (index 4)
        int kolonaDugme = 4;
        tblStavke.getColumnModel().getColumn(kolonaDugme).setCellRenderer(new ButtonRenderer());
        tblStavke.getColumnModel().getColumn(kolonaDugme).setCellEditor(new ButtonEditor(new JCheckBox()));

        // Sužavanje kolone sa dugmetom
        tblStavke.getColumnModel().getColumn(kolonaDugme).setPreferredWidth(40);
        tblStavke.getColumnModel().getColumn(kolonaDugme).setMaxWidth(60);
        tblStavke.getColumnModel().getColumn(kolonaDugme).setResizable(false);

        // Opcionalno: centriraj tekst u kolonama cijena i količine
        tblStavke.getColumnModel().getColumn(1).setCellRenderer(new DefaultTableCellRenderer() {
            { setHorizontalAlignment(SwingConstants.CENTER); }
        });
    }
    
