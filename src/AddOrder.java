    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String search = txtPretraga.getText().trim();
        if (search.isEmpty()) {
            ucitajSveLijekove();
            return;
        }

        try {
            Connection con = ConnectionProvider.getCon();
            PreparedStatement ps = con.prepareStatement(
                "SELECT id, atc_sifra, naziv, proizvodjac, cijena " +
                "FROM lijek WHERE naziv LIKE ? OR atc_sifra LIKE ? " +
                "ORDER BY naziv LIMIT 100"
            );
            String like = "%" + search + "%";
            ps.setString(1, like);
            ps.setString(2, like);

            ResultSet rs = ps.executeQuery();
            DefaultTableModel model = (DefaultTableModel) txtLijekovi.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("atc_sifra"),
                    rs.getString("naziv"),
                    rs.getString("proizvodjac"),
                    String.format("%.2f", rs.getDouble("cijena"))
                });
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(jDialog1, e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnSacuvajNarudzbuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSacuvajNarudzbuActionPerformed
        // TODO add your handling code here:
        if (cmbDobavljac.getSelectedItem() == null) {
            JOptionPane.showMessageDialog(this, "Odaberite dobavljača!");
            return;
        }

        if (stavkeModel.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "Dodajte barem jednu stavku!");
            return;
        }

        Connection con = null;
        try {
            con = ConnectionProvider.getCon();
            con.setAutoCommit(false);

            // 1. Spremi zaglavlje narudžbe
            PreparedStatement psZaglavlje = con.prepareStatement(
                "INSERT INTO narudzbe (dobavljac_id, datum_kreiranja, rok, status, napomena, ukupno) " +
                "VALUES (?, NOW(), ?, 'Kreirana', ?, ?)",
                Statement.RETURN_GENERATED_KEYS
            );

            // Dobavljač – pretpostavljam da si u comboboxu stavio samo naziv, treba ID
            // Ako nemaš ID u comboboxu, moraš promijeniti postaviComboboxDobavljace da koristi ComboItem
            // Za sada ćemo koristiti naziv – bolje je imati ID
            String dobavljacNaziv = cmbDobavljac.getSelectedItem().toString();
            // Pronađi ID po nazivu (nije idealno, ali radi za test)
            int dobavljacId = pronadjiDobavljacIdPoNazivu(dobavljacNaziv);

            psZaglavlje.setInt(1, dobavljacId);
            psZaglavlje.setDate(2, txtDatum.getDate() != null ? new java.sql.Date(txtDatum.getDate().getTime()) : null);
            psZaglavlje.setString(3, txtNapomena.getText().trim());

            double ukupnoNarudzbe = izracunajUkupnoNarudzbe();
            psZaglavlje.setDouble(4, ukupnoNarudzbe);
            psZaglavlje.executeUpdate();

            ResultSet rs = psZaglavlje.getGeneratedKeys();
            int narudzbaId = -1;
            if (rs.next()) {
                narudzbaId = rs.getInt(1);
            }

            // 2. Spremi sve stavke
            PreparedStatement psStavka = con.prepareStatement(
                "INSERT INTO narudzba_stavke (narudzba_id, lijek_id, kolicina_narucena, nabavna_cijena) " +
                "VALUES (?, ?, ?, ?)"
            );

            for (int i = 0; i < stavkeModel.getRowCount(); i++) {
                String nazivLijeka = stavkeModel.getValueAt(i, 0).toString();
                int kolicina = Integer.parseInt(stavkeModel.getValueAt(i, 1).toString());
                double cijena = Double.parseDouble(
                    stavkeModel.getValueAt(i, 2).toString().replace(" KM", "").replace(",", ".")
                );

                // Pronađi lijek_id po nazivu (nije idealno, bolje čuvati ID u modelu)
                int lijekId = pronadjiLijekIdPoNazivu(nazivLijeka);

                psStavka.setInt(1, narudzbaId);
                psStavka.setInt(2, lijekId);
                psStavka.setInt(3, kolicina);
                psStavka.setDouble(4, cijena);
                psStavka.executeUpdate();
            }

            con.commit();
            JOptionPane.showMessageDialog(this, "Narudžba uspješno sačuvana!");

            // Očisti formu ili zatvori
            dispose();
            new OrderMedicine(username).setVisible(true);

        } catch (Exception ex) {
            if (con != null) {
                try { con.rollback(); } catch (SQLException ignored) {}
            }
            JOptionPane.showMessageDialog(this, "Greška pri spremanju narudžbe:\n" + ex.getMessage());
            ex.printStackTrace();
        } finally {
            if (con != null) {
                try { con.setAutoCommit(true); con.close(); } catch (Exception ignored) {}
            }
        }
    }//GEN-LAST:event_btnSacuvajNarudzbuActionPerformed

    private void btnDialogDodajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDialogDodajActionPerformed
        // TODO add your handling code here:
        try {
            // 1. Provjeri da li je količina validna
            String kolicinaStr = txtKolicina.getText().trim();
            if (kolicinaStr.isEmpty()) {
                JOptionPane.showMessageDialog(jDialog1, "Unesite količinu!");
                return;
            }

            int kolicina = Integer.parseInt(kolicinaStr);
            if (kolicina <= 0) {
                JOptionPane.showMessageDialog(jDialog1, "Količina mora biti veća od 0!");
                return;
            }

            // 2. Uzmi cijenu (iz tabele ili polja)
            String cijenaStr = txtCijena.getText().replace(" KM", "").replace(",", ".");
            double cijena = Double.parseDouble(cijenaStr);

            double ukupno = kolicina * cijena;

            // 3. Dodaj red u glavnu tabelu stavki (tblStavke)
            stavkeModel.addRow(new Object[] {
                txtOdabrani.getText(),                  // Naziv lijeka
                kolicina,                               // Količina
                String.format("%.2f KM", cijena),       // Nabavna cijena
                String.format("%.2f KM", ukupno),       // Ukupno za stavku
                "X"                                     // Dugme za brisanje
            });

            // 4. Očisti polja u dialogu (opcionalno)
            txtOdabrani.setText("");
            txtCijena.setText("");
            txtKolicina.setText("");
            txtKolicina.setEnabled(false);
            btnDialogDodaj.setEnabled(false);

            // 5. Zatvori dialog nakon dodavanja
            jDialog1.dispose();

            // 6. Opcionalno: ažuriraj ukupnu sumu narudžbe
            azurirajUkupnoNarudzbe();

        } 
        catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(jDialog1, "Količina ili cijena nisu ispravni brojevi!");
        } 
        catch (HeadlessException ex) {
            JOptionPane.showMessageDialog(jDialog1, "Greška: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_btnDialogDodajActionPerformed

