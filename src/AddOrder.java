    private void postaviSelektovanjeLijeka() {
        txtLijekovi.getSelectionModel().addListSelectionListener(e -> {
            if (!e.getValueIsAdjusting()) {
                int row = txtLijekovi.getSelectedRow();
                if (row != -1) {
                    txtOdabrani.setText(txtLijekovi.getValueAt(row, 2).toString());

                    // Sigurnosna provjera da cijena nije null
                    Object cijenaObj = txtLijekovi.getValueAt(row, 4);
                    txtCijena.setText(cijenaObj != null ? cijenaObj.toString() : "0.00 KM");

                    txtKolicina.setEnabled(true);
                    btnDialogDodaj.setEnabled(true);
                } else {
                    txtOdabrani.setText("");
                    txtCijena.setText("");
                    txtKolicina.setEnabled(false);
                    btnDialogDodaj.setEnabled(false);
                }
            }
        });
    }

    
